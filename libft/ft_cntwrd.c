int		ft_cntwrd(const char *str, char c)
{
	int i;
	int j;

	j = 0;
	i = 0;
	if (!*str)
		return (0);
	while (str[i])
	{
		if (str[i] != c && str[i + 1] == c && str[i + 1])
			j++;
		i++;
	}
	if (str[i - 1] == c)
		j--;
	return (j + 1);
}
